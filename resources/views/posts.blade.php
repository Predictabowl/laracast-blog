                Ambientato tra 1628 e il 1630 in Lombardia, durante il dominio spagnolo, fu il primo esempio di romanzo storico della letteratura italiana. Il romanzo si basa su una rigorosa ricerca storica e gli episodi del XVII secolo, come ad esempio le vicende della monaca di Monza (Marianna de Leyva y Marino) e la Grande Peste del 1629–1631, si fondano su documenti d'archivio e cronache dell'epoca. Il romanzo di Manzoni viene considerato non solo una pietra miliare della letteratura italiana - in quanto è il primo romanzo moderno di questa tradizione letteraria - ma anche un passaggio fondamentale nella nascita stessa della lingua italiana[2].                 
